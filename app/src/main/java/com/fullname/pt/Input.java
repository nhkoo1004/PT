package com.fullname.pt;

public class Input {
    private String squat, push, jac, crunch, food, cal;

    public Input (){

    }

    public String getSquat() {
        return squat;
    }

    public void setSquat(String squat) {
        this.squat = squat;
    }

    public String getPush() {
        return push;
    }

    public void setPush(String push) {
        this.push = push;
    }

    public String getJac() {
        return jac;
    }

    public void setJac(String jac) {
        this.jac = jac;
    }

    public String getCrunch() {
        return crunch;
    }

    public void setCrunch(String crunch) {
        this.crunch = crunch;
    }

    public String getFood() {
        return food;
    }

    public void setFood(String food) {
        this.food = food;
    }

    public String getCal() {
        return cal;
    }

    public void setCal(String cal) {
        this.cal = cal;
    }
}
